import { readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';

const root = new URL('../', import.meta.url);
const args = process.argv.slice(2);
const check = args.includes('--check');
const sourceArgument = args.find((argument) => !argument.startsWith('--'));
const sourcePath = resolve(sourceArgument ?? 'public/openapi.yaml');
const contract = await readFile(sourcePath, 'utf8');
const pinnedVersion = (await readFile(new URL('CONTRACT_VERSION', root), 'utf8')).trim();
const version = contract.match(/^  version:\s*([^\s#]+)\s*$/m)?.[1];

if (!version || version !== pinnedVersion) {
  throw new Error(`OpenAPI version ${version ?? '<missing>'} does not match ${pinnedVersion}`);
}

const operations = [];
let currentPath = '';
let currentOperation;
for (const line of contract.split(/\r?\n/)) {
  const pathMatch = line.match(/^  (\/.*):\s*$/);
  if (pathMatch) {
    currentPath = pathMatch[1];
    currentOperation = undefined;
    continue;
  }
  const methodMatch = line.match(/^    (get|post|put|patch|delete|head):\s*$/);
  if (methodMatch && currentPath) {
    currentOperation = { method: methodMatch[1].toUpperCase(), path: currentPath };
    continue;
  }
  if (!currentOperation) continue;
  const tagsMatch = line.match(/^      tags:\s*\[([^\]]+)]\s*$/);
  if (tagsMatch) currentOperation.tag = tagsMatch[1].trim();
  const summaryMatch = line.match(/^      summary:\s*(.+?)\s*$/);
  if (summaryMatch) currentOperation.summary = unquote(summaryMatch[1]);
  const operationMatch = line.match(/^      operationId:\s*([^\s#]+)\s*$/);
  if (operationMatch) {
    currentOperation.operationId = operationMatch[1];
    if (!currentOperation.tag || !currentOperation.summary) {
      throw new Error(`Incomplete OpenAPI operation ${currentOperation.method} ${currentPath}`);
    }
    operations.push(currentOperation);
    currentOperation = undefined;
  }
}

if (operations.length === 0) throw new Error('No OpenAPI operations were found');
const operationIDs = new Set(operations.map(({ operationId }) => operationId));
if (operationIDs.size !== operations.length) throw new Error('OpenAPI operationId values are not unique');

const outputs = new Map([
  [
    new URL('src/content/docs/api-reference.md', root),
    renderReference({
      title: 'API 参考',
      description: `VoiceAsset Server OpenAPI ${version} 的自动生成操作索引。`,
      intro: `本页由公开 OpenAPI \`${version}\` 自动生成。以下载的契约作为请求、响应和错误模式的唯一权威来源。`,
      labels: { contract: '契约', operations: '操作数', download: '下载完整 OpenAPI YAML', method: '方法', path: '路径', operation: '操作 ID', summary: '摘要' },
    }),
  ],
  [
    new URL('src/content/docs/en/api-reference.md', root),
    renderReference({
      title: 'API Reference',
      description: `Generated operation index for VoiceAsset Server OpenAPI ${version}.`,
      intro: `This page is generated from public OpenAPI \`${version}\`. Treat the downloadable contract as the source of truth for request, response, and error schemas.`,
      labels: { contract: 'Contract', operations: 'Operations', download: 'Download the complete OpenAPI YAML', method: 'Method', path: 'Path', operation: 'Operation ID', summary: 'Summary' },
    }),
  ],
]);

if (check) {
  for (const [url, expected] of outputs) {
    const actual = await readFile(url, 'utf8');
    if (actual !== expected) throw new Error(`${url.pathname} is not synchronized with public/openapi.yaml`);
  }
  console.log(`Generated API reference verified: OpenAPI ${version}, ${operations.length} operations.`);
} else {
  await writeFile(new URL('public/openapi.yaml', root), contract);
  for (const [url, value] of outputs) await writeFile(url, value);
  console.log(`Generated API reference: OpenAPI ${version}, ${operations.length} operations.`);
}

function unquote(value) {
  if ((value.startsWith("'") && value.endsWith("'")) || (value.startsWith('"') && value.endsWith('"'))) {
    return value.slice(1, -1);
  }
  return value;
}

function escapeCell(value) {
  return value.replaceAll('|', '\\|').replaceAll('\n', ' ');
}

function renderReference({ title, description, intro, labels }) {
  const groups = new Map();
  for (const operation of operations) {
    if (!groups.has(operation.tag)) groups.set(operation.tag, []);
    groups.get(operation.tag).push(operation);
  }
  const sections = [...groups]
    .map(([tag, taggedOperations]) => {
      const rows = taggedOperations
        .map(({ method, path, operationId, summary }) => `| \`${method}\` | \`${escapeCell(path)}\` | \`${operationId}\` | ${escapeCell(summary)} |`)
        .join('\n');
      return `## ${tag}\n\n| ${labels.method} | ${labels.path} | ${labels.operation} | ${labels.summary} |\n| --- | --- | --- | --- |\n${rows}`;
    })
    .join('\n\n');
  return `---\ntitle: ${title}\ndescription: ${description}\n---\n\n<!-- Generated by scripts/generate-api-reference.mjs; do not edit. -->\n\n${intro}\n\n- ${labels.contract}: \`${version}\`\n- ${labels.operations}: \`${operations.length}\`\n- [${labels.download}](/openapi.yaml)\n\n${sections}\n`;
}
